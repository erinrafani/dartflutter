  //deklarasikan variable
  var a = 5;
  var b = 4;
  print(a.toString() + "==" + b.toString());
  print(a == b);
  print(a.toString() + "!=" + b.toString());
  print(a != b);
  print(a.toString() + ">" + b.toString());
  print(a > b);
  print(a.toString() + "<" + b.toString());
  print(a < b);
  print(a.toString() + " == " + b.toString() + " && " + a.toString() + " > " + b.toString());
  print((a == b) && (a > b ));
  print(a.toString() + " == " + b.toString() + " || " + a.toString() + " > " + b.toString());
  print((a == b) || (a > b ));
} 